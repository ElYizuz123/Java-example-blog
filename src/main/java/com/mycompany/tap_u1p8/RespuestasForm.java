    private void leerRespuestas() {
        Gson gson = new Gson();
        try {
            BufferedReader br = new BufferedReader(
                new FileReader(fileRespuestas)
            );
            String lectura = null;
            String contenido = "";
            while( (lectura = br.readLine()) != null ) {
                contenido += lectura;
            }
            br.close();
            
            JSONParser jsonParser = new JSONParser();
            JSONArray jsonArray = (JSONArray)jsonParser.parse(contenido);
            for (int i = 0; i < jsonArray.size(); i++) {
                Respuesta r = gson.fromJson(jsonArray.get(i).toString(), Respuesta.class);
                
                // Todas las respuestas
                respuestas.add(r);
                
                // Solo las respuestas al tema
                if(r.getIdTema().equals(tema.getIdTema())) {
                    respuestasTema.add(r);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }

    private void mostrarRespuestas() {
        for(Respuesta r : respuestasTema) {
            grid.setRows(grid.getRows()+1);
            
            PanelRespuesta panel = new PanelRespuesta(r);
            jPanel1.add(panel);
        }
        jPanel1.updateUI();
    }
